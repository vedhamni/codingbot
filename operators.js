let x = 7;
let y = 4;

// addition
console.log('x + y = ', x + y);  // 11

//subtraction
console.log('x - y = ', x - y); //3

//multiplication
console.log('x * y = ', x * y); //28

//division
console.log('x / y = ' , x / y); //1.75

//remainder
console.log('x % y = ' , x % y); //3

//exponentiation
console.log('x ** y = ' , x ** y); //2401